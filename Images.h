/* -----------------------------------------------------------------------------------------------------------------------------
 *  Retrieve the width of an image.
 * -----------------------------------------------------------------------------------------------------------------------------
 */
uint8_t getImageWidth(const uint8_t *image) {
  
  return pgm_read_byte(image);
  
}
  
  
/* -----------------------------------------------------------------------------------------------------------------------------
 *  Retrieve the height of an image.
 * -----------------------------------------------------------------------------------------------------------------------------
 */
 uint8_t getImageHeight(const uint8_t *image) {

  return pgm_read_byte(image + 1);

}


